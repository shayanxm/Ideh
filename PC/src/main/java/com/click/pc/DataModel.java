package com.click.pc;

public class DataModel {
    private String status;
    private String message;
    private Boolean show_register_form;
    private String code_Verifi;
    private String Mobile_number;
    private String OutCome;
    private String Xuser;
    private String UserName;
    private String Auth_token;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Boolean getShow_register_form() {
        return show_register_form;
    }

    public void setShow_register_form(Boolean show_register_form) {
        this.show_register_form = show_register_form;
    }

    public String getCode_Verifi() {
        return code_Verifi;
    }

    public void setCode_Verifi(String code_Verifi) {
        this.code_Verifi = code_Verifi;
    }

    public String getMobile_number() {
        return Mobile_number;
    }

    public void setMobile_number(String mobile_number) {
        Mobile_number = mobile_number;
    }

    public String getOutCome() {
        return OutCome;
    }

    public void setOutCome(String outCome) {
        OutCome = outCome;
    }

    public String getXuser() {
        return Xuser;
    }

    public void setXuser(String xuser) {
        Xuser = xuser;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        UserName = userName;
    }

    public String getAuth_token() {
        return Auth_token;
    }

    public void setAuth_token(String auth_token) {
        Auth_token = auth_token;
    }
}
